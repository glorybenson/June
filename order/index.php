    // handles ajax upload
    function uploadSupportingDocumentation(){

      var uploadButton = j(this);
      var uploadType = uploadButton.data('doc-type');
      var file_input = "file-upload-" + uploadType;
      var name = "Supporting Evidence (" + uploadType + ") for " + j('input[name=company-name]').val();

      // odd bug, tmp fix to prevent enter key triggering upload...
      if( uploadButton.is(':visible') == false){
        return false;
      }

      f.ShowLoader("Uploading document...", "loader-fixed");
      j(".attached-label-"+ uploadType).remove();

      j.ajaxFileUpload({
        url: '/ejax-template-upload/?elementid=' + file_input + "&docref=SUPPDOC&name=" + name,
        secureuri: false,
        fileElementId: file_input,
        dataType: 'json',
        success: function (data, status) {
            var response = data; // alias data -  jQ requires 'data' naming

            f.HideLoader();

            if( response.errors.length === 0){
              // put the label on attach button and show link
              j("#ef-" + uploadType + "-doc").val(response.data.id);
              j("." + uploadType + "-col").html("<span class=\"attached-label-"+uploadType+"\">"+ response.data.name +" <a class=\"button\" href=\"/efiling-map-document/?d=" + response.data.id + "\" target=\"_blank\">View</a></span>");
            }

            if( data.errors.length > 0){
                j("." + uploadType + "-col").html("<span class='ef-upload-error-text'>Error: " +data.errors[0]+ "</span>");
            }

        },
        error: function (data, status, e) {
          alert(e);
        }
      });

    }

    bindEvents();
    companyNameCheck();

  })(window, jQuery, FormBuilder);
</script>

