  (function(w, j, f){

    // bind dom events
    function bindEvents(){
      j('.js-attach-same-as').on( 'click', uploadSupportingDocumentation );
      j('.js-attach-sensitive-name').on( 'click', uploadSupportingDocumentation );
    }

    function companyNameCheck(){
      f.NameCheck( j('#ef-company-name').val() );
